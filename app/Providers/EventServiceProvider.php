            SendEmailVerificationNotification::class,
        ],

        // Passport 事件
        \Laravel\Passport\Events\AccessTokenCreated::class => [
            \App\Listeners\Auth\AccessTokenCreatedListener::class
        ],
    ];

    /**
     * Register any events for your application.
     *
     * @return void
     */
    public function boot()
    {
        parent::boot();

        //
    }
}
